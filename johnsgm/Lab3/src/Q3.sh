#!/bin/sh
echo "<html>"
sed -re 's/^(==){1}(.*)/\<h2\>\2\<\/h2\>/g' \
| sed -re 's/^(=){1}(.*)/\<h1\>\2\<\/h1\>/g' \
| sed -re 's/(^\*|([^\\])\*)(([^*]|\\\*)*)\*/\2\<em\>\3\<\/em\>/g' \
| sed -re 's/(^_|([^\\])_)(([^_]|\\_)*)_/\2\<em\>\3\<\/em\>/g' \
| sed -re 's/(^\[|([^\\])\[)(([^]]|\\\])*)\]\((([^\)]|\\\))*)\)/\2\<a href="\5"\>\3\<\/a\>/g'
echo "</html>"
exit
